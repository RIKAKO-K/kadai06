package com.helloworld.kadai06;

public class MYFIRSTSTORY {
    private  final  String name;
    private  final int age;

    public MYFIRSTSTORY(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
